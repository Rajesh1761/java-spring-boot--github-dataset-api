package com.hackerrank.github.request;

import com.hackerrank.github.model.Repo;

public class RepoDTO {
	private Long id;
	private String name;
	private String url;

	static RepoDTO convertFrom(Repo repo) {
		return new RepoDTO(repo.getId(), repo.getName(), repo.getUrl());
	}

	public RepoDTO(Long id, String name, String url) {
		super();
		this.id = id;
		this.name = name;
		this.url = url;
	}
	public RepoDTO(){
		
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((url == null) ? 0 : url.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		RepoDTO other = (RepoDTO) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (url == null) {
			if (other.url != null)
				return false;
		} else if (!url.equals(other.url))
			return false;
		return true;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	@Override
	public String toString() {
		return "RepoDTO [id=" + id + ", name=" + name + ", url=" + url + "]";
	}
	
	
	
}
